def main() -> None:
    if not JSONL_PATH.exists():
        raise SystemExit(
            f"Missing {JSONL_PATH}. The augmented dataset hasn't been downloaded yet."
        )

    from huggingface_hub import HfApi, whoami

    api = HfApi()
    try:
        user = whoami()
        print(f"Authenticated as: {user.get('name', '?')}")
    except Exception as e:
        raise SystemExit(
            "Not authenticated. Run `huggingface-cli login` or set HF_TOKEN."
        ) from e

    ensure_readme()

    print(f"Creating dataset repo at {REPO_ID} (if it does not exist)...")
    api.create_repo(
        repo_id=REPO_ID,
        repo_type="dataset",
        private=False,
        exist_ok=True,
    )

    patterns = ["augmented_train.jsonl", "augmented_combined.csv", "augmented_README.md"]
    # Rename README for HF (it expects README.md)
    import shutil
    hf_readme = DATASETS_DIR / "README.md"
    if not hf_readme.exists():
        shutil.copy(README_PATH, hf_readme)
    patterns.append("README.md")

    available = [p for p in patterns if (DATASETS_DIR / p).exists()]
    print(f"Uploading {len(available)} files to {REPO_ID}...")
    api.upload_folder(
        folder_path=str(DATASETS_DIR),
        repo_id=REPO_ID,
        repo_type="dataset",
        allow_patterns=available,
    )

    print(f"Done. View the dataset at: https://huggingface.co/datasets/{REPO_ID}")


if __name__ == "__main__":
    main()
